_GGUF_TYPE_F32 = 0
_GGUF_VAL_UINT32 = 4
_GGUF_VAL_STRING = 8


def _wstr(buf: bytearray, s: bytes | str) -> None:
    data = s.encode("utf-8") if isinstance(s, str) else s
    buf += struct.pack("<Q", len(data))
    buf += data


def _kv_u32(buf: bytearray, key: str, value: int) -> None:
    _wstr(buf, key)
    buf += struct.pack("<I", _GGUF_VAL_UINT32)
    buf += struct.pack("<I", int(value) & 0xFFFFFFFF)


def _kv_str(buf: bytearray, key: str, value: str) -> None:
    _wstr(buf, key)
    buf += struct.pack("<I", _GGUF_VAL_STRING)
    _wstr(buf, value)


def _wtinfo(
    buf: bytearray, name: str, shape: tuple[int, ...],
    ggml_type: int, rel_offset: int,
) -> None:
    _wstr(buf, name)
    buf += struct.pack("<I", len(shape))
    for d in shape:
        buf += struct.pack("<Q", int(d))
    buf += struct.pack("<I", int(ggml_type))
    buf += struct.pack("<Q", int(rel_offset))


def _align(x: int, a: int) -> int:
    return (x + a - 1) & ~(a - 1)


def _build_fixture_gguf(path: Path, n_blocks: int = 2) -> None:
    alignment = 32
    tensors: list[tuple[str, tuple[int, ...]]] = [("token_embd.weight", (16,))]
    for i in range(n_blocks):
        tensors.append((f"blk.{i}.attn_q.weight", (8,)))
    tensors.append(("output.weight", (16,)))
    kv_buf = bytearray()
    _kv_u32(kv_buf, "general.alignment", alignment)
    _kv_str(kv_buf, "general.architecture", "llama")
    _kv_str(kv_buf, "tokenizer.ggml.model", "llama")
    _kv_str(kv_buf, "tokenizer.ggml.bos_token", "<s>")
    rel: list[int] = []
    cursor = 0
    tinfo = bytearray()
    for _n, shape in tensors:
        rel.append(cursor)
        n = 1
        for d in shape:
            n *= d
        cursor += n * 4
        cursor = _align(cursor, alignment)
    for (name, shape), ro in zip(tensors, rel):
        _wtinfo(tinfo, name, shape, _GGUF_TYPE_F32, ro)
    head = bytearray()
    head += b"GGUF"
    head += struct.pack("<I", 3)
    head += struct.pack("<Q", len(tensors))
    head += struct.pack("<Q", 4)
    pre = bytes(head) + bytes(kv_buf) + bytes(tinfo)
    data_start = _align(len(pre), alignment)
    buf = bytearray(max(data_start, len(pre)) + cursor)
    buf[: len(pre)] = pre
    for (_n, shape), ro in zip(tensors, rel):
        n = 1
        for d in shape:
            n *= d
        floats = [((i + 1) * 0.5) for i in range(n)]
        packed = struct.pack("<" + "f" * n, *floats)
        off = data_start + ro
        buf[off: off + len(packed)] = packed
    last_n = 1
    for d in tensors[-1][1]:
        last_n *= d
    end = data_start + rel[-1] + last_n * 4
    path.write_bytes(bytes(buf[:end]))


