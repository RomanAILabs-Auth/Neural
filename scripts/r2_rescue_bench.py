"""Phase 11 — honest R2 Omega n-gram rescue benchmark.

Rationale.
    The Final-Product ``bench-wps`` headline reports the raw
    ``effective_wps`` and segments R2 into exact-state vs rescue. That
    number shows "how much throughput did the rescue contribute on
    this run" -- it does NOT tell you "does the rescue actually match
    sensible prompts to sensible answers, or is it firing on any
    trigram collision?". This script isolates the latter question so
    the rescue quality is auditable independent of decode speed.

Method.
    1. Seed a ZPM index with ~20 hand-written NRL architecture Q&A
       pairs. Each entry carries ``prompt_head`` stamped exactly the
       way the live R5 writeback path in ``gguf.py`` writes it today.
       States are written with the R1 anchor convention, matching
       what ``run_gguf`` persists -- not the R2 Omega-folded state.
       This is important: it faithfully models the production
       write/read mismatch that the rescue path is designed to
       bridge.
    2. For every seed, generate several *rephrasings* (paraphrases,
       question-to-statement rewrites, partial overlaps). These are
       curated by hand, not generated by an LLM, so the baseline is
       human-intelligible.
    3. For every rephrasing, call ``try_omega_native_resolve`` in
       active mode with the rephrased prompt. The primary lookup
       will miss by construction (R2 state != stored R1 state). The
       rescue fires on prompt_head overlap. We record the entry it
       matched so the reviewer can eyeball the rescue quality.
    4. Emit a summary with per-seed rescue rate, the distribution of
       overlap scores, and a full audit trail: query -> matched
       ``prompt_head`` -> reply_text.

What this does NOT measure.
    * Wall-clock throughput. Use ``nrlpy bench-wps <model.gguf>`` for
      that. This script runs on the tiny GGUF fixture -- the LMO is
      trivial and the Omega probe is cheap, so timings here are not
      representative of real-model throughput.
    * End-to-end chat coherence. A rescue serving a sensible
      prompt->reply pair does NOT prove the reply is appropriate for
      every plausible extension of the prompt. That's what Stage-VI
      was for in the non-rescue path; rescues deliberately skip
      bit-symmetry. This script reports rescue semantic quality (via
      prompt_head audit) but cannot prove deep reply-level coherence.
"""

