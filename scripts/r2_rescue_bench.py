@dataclass
class RescueRecord:
    """Per-query outcome for the audit trail."""

    seed_prompt: str
    query: str
    served: bool
    ngram_rescued: bool
    overlap: float
    distance_bits: int
    matched_prompt_head: str
    matched_reply_head: str
    demotion_reasons: tuple[str, ...] = ()


@dataclass
class RescueBenchReport:
    """Summary + audit trail."""

    total_queries: int
    total_seeds: int
    total_rephrases: int
    served_total: int
    rescued_total: int
    # share = served / total_queries. rescue_share = rescued / served.
    served_share: float
    rescue_share: float
    # overlap distribution on rescued queries
    overlap_min: float = 0.0
    overlap_p50: float = 0.0
    overlap_max: float = 0.0
    audit: list[RescueRecord] = field(default_factory=list)

    def to_dict(self) -> dict[str, object]:
        d = {
            "total_queries": self.total_queries,
            "total_seeds": self.total_seeds,
            "total_rephrases": self.total_rephrases,
            "served_total": self.served_total,
            "rescued_total": self.rescued_total,
            "served_share": self.served_share,
            "rescue_share": self.rescue_share,
            "overlap_min": self.overlap_min,
            "overlap_p50": self.overlap_p50,
            "overlap_max": self.overlap_max,
            "audit": [r.__dict__ for r in self.audit],
        }
        return d


def _percentile(xs: list[float], p: float) -> float:
    if not xs:
        return 0.0
    s = sorted(xs)
    k = int(round((p / 100.0) * (len(s) - 1)))
    return s[max(0, min(len(s) - 1, k))]


def _seed_zpm_for_corpus(
    manifest: gguf.GgufManifest,
    corpus: tuple[Seed, ...],
) -> zpm.ZpmIndex:
    """Build an in-memory ZPM index with one entry per seed.

    The state is computed with the same anchor convention the live R5
    writeback path in ``gguf.py`` uses -- ``zpm.anchor(_zpm_anchor_bytes(...))``
    -- so this faithfully models what a real chat session accumulates.
    The prompt_head metadata is stamped identically as well. The index
    is kept in memory only; we never touch the user's on-disk ZPM
    cache.
    """
    idx = zpm.ZpmIndex()
    for seed in corpus:
        state = zpm.anchor(gguf._zpm_anchor_bytes(manifest, seed.prompt))
        idx.add(
            zpm.ZpmEntry(
                state=state,
                reply_text=seed.reply,
                tokens=max(1, len(seed.reply.split())),
                wall_s_at_write=0.0001,
                metadata={
                    "model": Path(manifest.model).name,
                    "chat_format": manifest.chat_format,
                    "seed": str(manifest.seed),
                    "prompt_head": seed.prompt[:256],
                    "src": "r2_rescue_bench",
                },
            )
        )
    return idx


def _run_bench(
    *,
    model_path: Path,
    nrl_root: Path,
    corpus: tuple[Seed, ...],
    zpm_threshold_bits: int,
    omega_budget_ms: float,
    verbose: bool,
) -> RescueBenchReport:
    # Absorb the GGUF into an LMO handle. ``absorb_gguf`` is content-
    # addressed on ``sha256(gguf)`` so a previously-absorbed model is
    # a fast cache hit; a first absorption of a multi-GB model is
    # proportional to disk-read throughput and is a one-time cost.
    print(f"  [bench] absorbing LMO from {model_path.name} ...", flush=True)
    t_absorb = time.perf_counter()
    absorbed = lmo.absorb_gguf(model_path, attempt_libllama=False)
    print(
        f"  [bench] LMO ready in {time.perf_counter() - t_absorb:.2f}s "
        f"(sha={absorbed.header.model_sha256[:12]}...)",
        flush=True,
    )

    # Build a throwaway manifest anchored to the same model. We skip
    # the ``run_gguf`` bootstrap -- that would trigger a real decode
    # against libllama for a 2 GB model -- and compute the model sha
    # directly. Every helper in this script only needs the sha and
    # manifest-level anchor bytes; neither needs a live decoder.
    manifest = gguf.manifest_from_args(
        str(model_path),
        prompt="bench-bootstrap",
        max_tokens=3,
        seed=1,
        muscle_memory="off",
        coherence_lane="max-throughput",
        r2_shadow_enabled=True,
        omega_budget_ms=omega_budget_ms,
    )
    # zpm_threshold_bits is not accepted by manifest_from_args; set it
    # post-construction (same pattern as rewired-mode's zpm_nullspace).
    manifest.zpm_threshold_bits = int(zpm_threshold_bits)
    # Populate model_sha256 directly -- no decode needed.
    manifest.model_sha256 = absorbed.header.model_sha256

    zidx = _seed_zpm_for_corpus(manifest, corpus)

    audit: list[RescueRecord] = []
    served = 0
    rescued = 0
    overlaps: list[float] = []
    total_rephrases = 0

    for seed in corpus:
        for rephrase in seed.rephrases:
            total_rephrases += 1
            intent_bytes = gguf._zpm_anchor_bytes(manifest, rephrase)
            rung, rep = try_omega_native_resolve(
                absorbed,
                intent_anchor_bytes=intent_bytes,
                coherence_lane="max-throughput",
                zpm_index=zidx,
                zpm_threshold_bits=zpm_threshold_bits,
                omega_budget_ms=omega_budget_ms,
                mode="active",
                prompt_text=rephrase,
            )
            matched_head = ""
            matched_reply = ""
            if rep.stored_entry_index >= 0:
                entries = list(zidx)
                if 0 <= rep.stored_entry_index < len(entries):
                    e = entries[rep.stored_entry_index]
                    matched_head = e.metadata.get("prompt_head", "")
                    matched_reply = e.reply_text[:120]
            rec = RescueRecord(
                seed_prompt=seed.prompt,
                query=rephrase,
                served=rep.served,
                ngram_rescued=rep.ngram_rescued,
                overlap=rep.ngram_best_overlap,
                distance_bits=rep.zpm_distance_bits,
                matched_prompt_head=matched_head,
                matched_reply_head=matched_reply,
                demotion_reasons=rep.demotion_reasons,
            )
            audit.append(rec)
            if rep.served:
                served += 1
            if rep.ngram_rescued:
                rescued += 1
                overlaps.append(rep.ngram_best_overlap)
            if verbose:
                print(
                    f"  seed={seed.prompt[:40]!r:<44}  query={rephrase[:40]!r:<44}  "
                    f"served={rep.served} rescued={rep.ngram_rescued} "
                    f"overlap={rep.ngram_best_overlap:.2f}"
                )

    total_queries = total_rephrases
    return RescueBenchReport(
        total_queries=total_queries,
        total_seeds=len(corpus),
        total_rephrases=total_rephrases,
        served_total=served,
        rescued_total=rescued,
        served_share=(served / total_queries) if total_queries else 0.0,
        rescue_share=(rescued / served) if served else 0.0,
        overlap_min=min(overlaps) if overlaps else 0.0,
        overlap_p50=_percentile(overlaps, 50.0),
        overlap_max=max(overlaps) if overlaps else 0.0,
        audit=audit,
    )


def _format_report(r: RescueBenchReport, *, show_audit: bool) -> str:
    lines: list[str] = []
    lines.append("=" * 78)
    lines.append("NRL Phase 11 — R2 Omega n-gram rescue benchmark")
    lines.append(f"  seeds                   : {r.total_seeds}")
    lines.append(f"  rephrased queries       : {r.total_queries}")
    lines.append(f"  overlap threshold       : {_NGRAM_OVERLAP_THRESHOLD:.2f}")
    lines.append("-" * 78)
    lines.append(
        f"  served_total            : {r.served_total} of {r.total_queries}  "
        f"({100.0 * r.served_share:.1f}% served share)"
    )
    lines.append(
        f"  ngram_rescued_total     : {r.rescued_total} of {r.served_total}  "
        f"({100.0 * r.rescue_share:.1f}% of served via rescue)"
    )
    if r.rescued_total > 0:
        lines.append(
            f"  overlap min/p50/max     : "
            f"{r.overlap_min:.2f} / {r.overlap_p50:.2f} / {r.overlap_max:.2f}"
        )
    lines.append("=" * 78)

    if show_audit:
        lines.append("")
        lines.append("Per-query audit (query -> matched prompt_head -> reply head):")
        lines.append("-" * 78)
        for rec in r.audit:
            status = (
                "RESCUED" if rec.ngram_rescued
                else "SERVED " if rec.served
                else "DEMOTED"
            )
            lines.append(
                f"  [{status}] query={rec.query!r}"
            )
            if rec.served or rec.ngram_rescued:
                lines.append(
                    f"    -> prompt_head={rec.matched_prompt_head!r}"
                )
                lines.append(
                    f"    -> reply_head ={rec.matched_reply_head!r}"
                )
                lines.append(
                    f"    -> overlap={rec.overlap:.2f}  "
                    f"distance={rec.distance_bits}b"
                )
            else:
                lines.append(
                    f"    -> demoted reasons: {', '.join(rec.demotion_reasons)}"
                )
            lines.append("")
        lines.append("=" * 78)
    return "\n".join(lines) + "\n"


# --------------------------------------------------------------------------- #
# CLI
# --------------------------------------------------------------------------- #


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(
        prog="r2_rescue_bench",
        description=(
            "Audit R2 Omega n-gram rescue quality on a curated NRL Q&A "
            "corpus. Measures rescue fire-rate, overlap distribution, "
            "and prints the prompt_head/reply audit trail."
        ),
    )
    ap.add_argument(
        "--model",
        type=Path,
        default=None,
        help=(
            "Path to a GGUF file. If omitted, a tiny self-contained "
            "fixture is used (sufficient for the rescue logic audit; "
            "NOT for throughput numbers -- use `nrlpy bench-wps` for "
            "those)."
        ),
    )
    ap.add_argument(
        "--zpm-threshold-bits",
        type=int,
        default=28,
        help="Hamming tolerance for ZPM primary lookup. Default: 28 (rewired).",
    )
    ap.add_argument(
        "--omega-budget-ms",
        type=float,
        default=12.0,
        help="Omega probe wall budget. Default: 12.0ms (rewired).",
    )
    ap.add_argument(
        "--audit",
        action="store_true",
        help="Print the full per-query audit trail.",
    )
    ap.add_argument(
        "--json",
        action="store_true",
        help="Emit the full report as JSON.",
    )
    ap.add_argument(
        "--verbose",
        action="store_true",
        help="Print one line per query while running.",
    )
    args = ap.parse_args(argv)

    import os
    import tempfile

    tmp = Path(tempfile.mkdtemp(prefix="nrl_r2_rescue_bench_"))

    if args.model is None:
        # Fixture mode: isolate NRL_ROOT to the tmpdir and force stub
        # inference so the test fixture never tries real decode.
        os.environ["NRL_ROOT"] = str(tmp)
        os.environ["NRL_INFERENCE"] = "stub"
        fx = tmp / "fixture.gguf"
        _build_fixture_gguf(fx, n_blocks=2)
        model_path = fx
    else:
        # Real-model mode: respect the user's existing NRL_ROOT so the
        # LMO cache hits on repeat runs (absorption is content-
        # addressed; a first absorption of a multi-GB GGUF is the slow
        # step). The ZPM index is built in memory only, never written
        # to the user's on-disk cache.
        model_path = args.model
        if not model_path.is_file():
            print(f"error: model not found: {model_path}", file=sys.stderr)
            return 2

    t0 = time.perf_counter()
    report = _run_bench(
        model_path=model_path,
        nrl_root=tmp,
        corpus=_SEED_CORPUS,
        zpm_threshold_bits=args.zpm_threshold_bits,
        omega_budget_ms=args.omega_budget_ms,
        verbose=args.verbose,
    )
    elapsed = time.perf_counter() - t0

    if args.json:
        out = report.to_dict()
        out["wall_clock_s"] = elapsed
        out["nrl_root"] = str(tmp)
        out["model_path"] = str(model_path)
        print(json.dumps(out, indent=2, default=str))
    else:
        print(_format_report(report, show_audit=args.audit))
        print(f"  wall_clock_s            : {elapsed:.3f}")
        print(f"  nrl_root (isolated)     : {tmp}")
        print(f"  model_path              : {model_path}")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
